#include <stdio.h>

int main(){
int num1;
int num2;
printf("Informe 2 numeros:");
scanf("%d%d",&num1,&num2);
printf("\n%d + %d = %d",num1,num2,num1+num2);
printf("\n%d - %d = %d",num1,num2,num1-num2);
printf("\n%d x %d = %d",num1,num2,num1*num2);
printf("\n%d / %d = %d",num1,num2,num1/num2);
return 0;
}
